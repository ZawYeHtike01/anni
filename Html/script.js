const song = document.getElementById('song');
const lyricsContainer = document.getElementById('lyrics-container');

let currentLine = 0;

const displayLyrics = () => {
    if (currentLine < lyrics.length) {
        const currentTime = song.currentTime;
        const nextLine = lyrics[currentLine];
        
        if (currentTime >= nextLine.time) {
            lyricsContainer.innerHTML = `<div class="lyric-line show">${nextLine.text}</div>`;
            currentLine++;
        }
    }
};

song.addEventListener('timeupdate', displayLyrics);